package ejercicioCuadrado;

public class triangulo {

	public static void main(String[] args) {
		int base=4;
		int altura=1;
		for(int i = 0; i<altura; i++) {
			System.out.println("*");
			for (int j = 0; j <base; j++) {
				System.out.println(" ");
				System.out.println("\t");
				
			}
		}

	}

}
